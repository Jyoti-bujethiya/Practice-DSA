class Solution
{
    public:
    Node* reverseBetween(Node* head, int m, int n)
    {
        //code here
        Node *ptr = head, *prev = head, *tmp1 = NULL, *tmp2 = head;
        while(ptr){
            if(m == 1){
                while(ptr && n>0){
                    Node *tmp = ptr->next;
                    ptr->next = prev;
                    prev = ptr;
                    ptr = tmp;
                    n -= 1;
                }
                if(tmp1 == NULL){
                    tmp1 = prev;
                    head = prev;
                }
                else
                    tmp1->next = prev;
                tmp2->next = ptr;
                break;
            }
            m -= 1;
            n -= 1;
            tmp1 = ptr;
            tmp2 = ptr->next;
            ptr = ptr->next;
        }
        return head;
    }
};

